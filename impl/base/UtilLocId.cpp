bool UtilLocId::LocIdSetContains(std::unordered_set<LocId,LocId>& _set, LocId locId){
  return !UtilLocId::LocIdSetNotContains(_set, locId);
}

bool UtilLocId::LocIdSetNotContains(std::unordered_set<LocId,LocId>& _set, LocId locId){
  bool contains=(_set.count(locId) <= 0);
  return contains;
}