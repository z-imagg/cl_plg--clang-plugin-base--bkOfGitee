bool UtilEnvVar::envVarEq(std::string varName, std::string varValueExpect){
  if(varName.empty()){
    return false;
  }
  const char* varValueReal= getenv(varName.c_str());
  if(varValueReal == NULL){
    return false;
  }
  bool eq= (varValueExpect == varValueReal);
  return eq;
}